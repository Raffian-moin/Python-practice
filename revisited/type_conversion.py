"""
to know the type of a variable we use type() method.
to convert to integer use int()
to convert to float use float()
to convert to boolean use bool()
to convert to string use str()
"""

st="string"
integer=12
fl=0.12
bl=True

print(type(st))
print(type(integer))
print(type(fl))
print(type(bl))

print(int(fl))
print(float(integer))
print(str(integer))
print(bool(fl))